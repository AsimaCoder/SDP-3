class SnapchatService {
    void sendSnapchatMessage(String message, String recipient) {
        System.out.println("Sending Snapchat message to " + recipient + ": " + message);
    }

    void sendSnapchatEmoji(String emoji, String recipient) {
        System.out.println("Sending Snapchat emoji to " + recipient + ": " + emoji);
    }
}
